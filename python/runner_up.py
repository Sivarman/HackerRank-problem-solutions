# Problem: Find the Runner-Up Score!
# Link: https://www.hackerrank.com/challenges/find-second-maximum-number-in-a-list/problem
# Description:
# Given the scores of participants, find the runner-up score (second highest).
# Input:
#   n - number of scores
#   arr - list of scores
# Output:
#   runner-up score

if __name__ == '__main__':
    n = int(input())
    arr = map(int, input().split())
    nums = list(sorted(set(arr)))
    print(nums[-2])
