# Problem: Arithmetic Operators
# Link: https://www.hackerrank.com/challenges/python-arithmetic-operators/problem
# Description:
# Given two integers a and b, perform the following operations:
# 1. Add a and b
# 2. Subtract b from a
# 3. Multiply a and b
# Input:
# Two integers, a and b (1 ≤ a, b ≤ 10^10)
# Output:
# Three lines: a + b, a - b, a * b

if __name__ == '__main__':
    a = int(input())
    b = int(input())
    print(a + b)
    print(a - b)
    print(a * b)
