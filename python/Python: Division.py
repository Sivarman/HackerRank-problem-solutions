# Problem: Python: Division
# Link: https://www.hackerrank.com/challenges/python-division/problem
# Description:
# Given two integers a and b, perform:
# 1. Integer division (a // b)
# 2. Float division (a / b)
# Input:
# Two integers, a and b (1 ≤ a, b ≤ 10^10)
# Output:
# Two lines: first line is integer division, second line is float division

if __name__ == '__main__':
    a = int(input())
    b = int(input())
    print(a // b)
    print(a / b)
